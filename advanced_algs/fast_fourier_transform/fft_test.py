import unittest
from fft import fft, polynomial_multiplication


class Test_Case_FFT(unittest.TestCase):
    def test_fft(self):
        p = fft([(1, 0), (2, 0), (3, 0), (4, 0), (0, 0),
                (0, 0), (0, 0), (0, 0)], None, 8, False)
        polars = [(10.0, 0.0),
                  (7.254475652862137, 1.6279250537906595),
                  (2.8284271247461903, -2.3561944901923453),
                  (2.715250080937386, 0.47535305657499183),
                  (2.0, 3.141592653589793),
                  (2.715250080937386, -0.47535305657499227),
                  (2.8284271247461903, 2.356194490192345),
                  (7.254475652862136, -1.6279250537906598),]
        for i, pz in enumerate(p):
            self.assertEqual(pz, polars[i])
        polar = polynomial_multiplication([(1, 0), (2, 0), (3, 0), (4, 0), (0, 0), (0, 0), (0, 0), (0, 0)], [
                                          (2, 0), (1, 0), (5, 0), (4, 0), (0, 0), (0, 0), (0, 0), (0, 0)])
        answer = [(16.0, -7.34788079488412e-16), (40.000000000000014, 2.449293598294706e-17), (103.99999999999997, 4.7101799967205974e-17), (200.0, -3.1106028698342777e-16), (216.0, 5.896447551450219e-17), (256.0, -9.950255243072243e-17), (128.00000000000003, -3.061616997868388e-17), (8.592879655495274e-14, 1.7369390453789255)]
        for i,pz in enumerate(polar):
            self.assertEqual(pz, answer[i])


if __name__ == '__main__':
    unittest.main()
